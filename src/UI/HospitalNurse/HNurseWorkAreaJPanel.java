    private void assignjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignjButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = nurseRquestjTable.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }

        WorkRequest request = (WorkRequest) nurseRquestjTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateNurseReqTable();
    }//GEN-LAST:event_assignjButton1ActionPerformed

    private void sendjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = nurseRquestjTable.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }

        WorkRequest request = (WorkRequest) nurseRquestjTable.getValueAt(selectedRow, 0);
        NurseWorkRequest nwr = (NurseWorkRequest) request;
        nwr.getEncounter().getVitalSigns().setBloodPressure(Double.parseDouble(bptextfield.getText()));
        nwr.getEncounter().getVitalSigns().setHeartRate(Double.parseDouble(hejTextField.getText()));
        nwr.getEncounter().getVitalSigns().setPulseRate(Double.parseDouble(pulsejTextField.getText()));
        nwr.getEncounter().getVitalSigns().setWeight(Double.parseDouble(wtjTextField3.getText()));
        nwr.setStatus("Report Sent");
        populateNurseReqTable();


    }//GEN-LAST:event_sendjButton2ActionPerformed

