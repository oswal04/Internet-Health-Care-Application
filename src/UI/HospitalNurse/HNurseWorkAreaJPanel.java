 public void populateNurseReqTable() {
        DefaultTableModel model = (DefaultTableModel) nurseRquestjTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : hospitalNurseOrganization.getWorkQueue().getWorkList()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getEmployee();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee();
            NurseWorkRequest nwr = (NurseWorkRequest) request;
            row[3] = nwr.getPatient();
            row[4] = request.getStatus();
            row[5] = nwr.getEncounter().getVitalSigns().getBloodPressure();
            model.addRow(row);
        }
    }
