    private void populateCallbackTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        if (pharmacistOrganization instanceof PharmacistOrganization) {
            for (WorkRequest workRequest : pharmacistOrganization.getWorkQueue().getWorkList()) {
                if (workRequest instanceof PC_PharmacyWorkRequest) {
                    PC_PharmacyWorkRequest pcpwr = (PC_PharmacyWorkRequest) workRequest;
                    Object[] row = new Object[10];
                    row[0] = pcpwr.getD();
                    row[1] = pcpwr.getD().getDrugNo();
                    row[2] = pcpwr.getD().getManufacturer();
                    row[3] = pcpwr.getMessage();
                    row[4] = pcpwr.getRequestDate();
                    model.addRow(row);
                }
            }
        }
    }
