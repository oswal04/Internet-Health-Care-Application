    private void populateDrugTable() {
        DefaultTableModel model = (DefaultTableModel) drugjTable.getModel();
        model.setRowCount(0);
        PharmaceuticalCompanyEnterprise pce = (PharmaceuticalCompanyEnterprise) enterprise;
        for (Drug d : pce.getDrugCatalog().getDrugList()) {
            Object[] row = new Object[10];
            row[0] = d.getDrugNo();
            row[1] = d.getDrugType();
            row[2] = d;
            row[3] = d.getCompoundName();
            row[4] = d.getAdditives();
            row[5] = d.getDosage();
            row[6] = d.getSide_effects();
            row[7] = d.getExpDate();
            row[8] = d.getApprovalStatus();

            model.addRow(row);
        }
    }

    private void populateReqTable() {
        DefaultTableModel model = (DefaultTableModel) reqjTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkList()) {
            //System.out.println(request);
            if (request instanceof PC_FdaWorkRequest) {
                PC_FdaWorkRequest pcfwr = (PC_FdaWorkRequest) request;
                if (pcfwr.getSenderCo().equals(enterprise)) {
                    Object[] row = new Object[7];
                    row[0] = pcfwr.getDrug();
                    row[1] = pcfwr.getSenderCo();
                    row[2] = pcfwr.getSender();
                    row[3] = pcfwr.getRequestDate();
                    row[4] = pcfwr.getReceiver();
                    row[5] = pcfwr.getDrug().getApprovalStatus();
                    row[6] = pcfwr.getResolveDate();
                    model.addRow(row);

                }
            }
        }
        populateDrugTable();
    }

    private void popDrugType() {
        drugtypejComboBox.removeAllItems();
        for (Drug.DrugType type : Drug.DrugType.values()) {
            drugtypejComboBox.addItem(type);
        }
    }
