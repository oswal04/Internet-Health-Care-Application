        // TODO add your handling code here:
        String mname = mednamejTextField1.getText();
       if(Pattern.matches("[a-zA-Z]+", mname)) 
       {}
       else
       {
      JOptionPane.showConfirmDialog(null, "Please enter Medicine Name", null, JOptionPane.CANCEL_OPTION);
      return;
       } 
       
       String coname = compoundsjTextField.getText();
       if(Pattern.matches("[a-zA-Z]+", coname)) 
       {}
       else
       {
      JOptionPane.showConfirmDialog(null, "Please enter Compound Name", null, JOptionPane.CANCEL_OPTION);
      return;
       }   
       
       String addname = additivesjTextField.getText();
       if(Pattern.matches("[a-zA-Z]+", addname)) 
       {}
       else
       {
      JOptionPane.showConfirmDialog(null, "Please enter Additives Name", null, JOptionPane.CANCEL_OPTION);
      return;
       }   
       
       String sidname = sideeffectsTextField1.getText();
       if(Pattern.matches("[a-zA-Z]+", sidname)) 
       {}
       else
       {
      JOptionPane.showConfirmDialog(null, "Please enter Side Effect Name", null, JOptionPane.CANCEL_OPTION);
      return;
       }   
       
       
        Drug d = new Drug();
        d.setMedicineName(mednamejTextField1.getText());
        d.setCompoundName(compoundsjTextField.getText());
        d.setAdditives(additivesjTextField.getText());
        d.setExpDate(expdatejTextField.getText());
        d.setManufacturer(enterprise.getName());
        d.setDosage("Children: " + dcjTextField.getText() + "\n" + "Adults:" + dajTextField1.getText());
        d.setSide_effects(sideeffectsTextField1.getText());
        d.setDrugType((Drug.DrugType) drugtypejComboBox.getSelectedItem());
        d.setGenericName(gnamejTextField1.getText());
        if (enterprise instanceof PharmaceuticalCompanyEnterprise) {
            PharmaceuticalCompanyEnterprise pce = (PharmaceuticalCompanyEnterprise) enterprise;
            pce.getDrugCatalog().addDrug(d);

        }

        populateDrugTable();
