    private void sendjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButton2ActionPerformed
        // TODO add your handling code here:

        PC_FdaWorkRequest pcfwr = new PC_FdaWorkRequest();
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("HH:mm:ss  MMM/dd/yyyy");
        String reqDate = (dateFormat.format(date));

        int selectedRow = drugjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please choose a drug");
            return;

        }
        Drug d = (Drug) drugjTable.getValueAt(selectedRow, 2);
        pcfwr.setSender(userAccount);
        pcfwr.setSenderCo(enterprise);
        pcfwr.setRequestDate(reqDate);
        pcfwr.setResolveDate(null);
        pcfwr.setStatus("Sent to FDA");
        d.setApprovalStatus("Sent to FDA");
        pcfwr.setDrug(d);

        Organization org = null;
        for (Network n : ecosystem.getcountryNetworkList()) {
            for (StateNetwork sn : n.getStateNetworkList()) {
                for (CityNetwork cn : sn.getCityNetworkList()) {
                    for (Enterprise e : cn.getEnterpriseDirectory().getEnterpriseList()) {
                        for (Organization organization : e.getOrganizationDirectory().getOrganizationList()) {
                            System.out.println(organization);
                            if (organization instanceof FDAMgrOrganization) {

                                org = organization;
                            }
                        }
                    }
                }
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkList().add(pcfwr);
            userAccount.getWorkQueue().getWorkList().add(pcfwr);
        }
        populateDrugTable();
        populateReqTable();
    }//GEN-LAST:event_sendjButton2ActionPerformed

