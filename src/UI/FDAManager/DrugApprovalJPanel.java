    private void appdrugjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_appdrugjButtonActionPerformed
        // TODO add your handling code here:
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("HH:mm:ss  MMM/dd/yyyy");
        String resDate = (dateFormat.format(date));
        int selectedRow = reqjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select A Drug!!!");
            return;
        }

        WorkRequest request = (WorkRequest) reqjTable.getValueAt(selectedRow, 0);
        if (request instanceof PC_FdaWorkRequest) {
            PC_FdaWorkRequest pcfwr = (PC_FdaWorkRequest) request;
            pcfwr.setReceiver(userAccount);
            pcfwr.setResolveDate(resDate);
            //setting attributes of the drug
            pcfwr.getDrug().setApprovalStatus("Approved");
            pcfwr.getDrug().setApprovedOn(resDate);
            //add drug to fda approved list
            fdaEnterprise.getDrugCatalog().getDrugList().add(pcfwr.getDrug());

            populateReqTable();
    }//GEN-LAST:event_appdrugjButtonActionPerformed
    }
