    private void populateReqTable() {
        DefaultTableModel model = (DefaultTableModel) reqjTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : fDAMgrOrganization.getWorkQueue().getWorkList()) {
            if (request instanceof PC_FdaWorkRequest) {
                PC_FdaWorkRequest pcfwr = (PC_FdaWorkRequest) request;
                Object[] row = new Object[9];
                row[0] = pcfwr;
                row[1] = pcfwr.getDrug().getDrugType();
                row[2] = pcfwr.getSenderCo();
                row[3] = pcfwr.getSender().getEmployee();
                row[4] = pcfwr.getRequestDate();
                row[5] = request.getReceiver() == null ? null : request.getReceiver().getEmployee();
                row[6] = pcfwr.getDrug().getApprovalStatus();
                row[7] = request.getResolveDate() == null ? null : request.getResolveDate();
                model.addRow(row);
            }
        }
    }
