    private void sendjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButton1ActionPerformed
        // TODO add your handling code here:

        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("HH:mm:ss  MMM/dd/yyyy");
        String reqDate = (dateFormat.format(date));
        FDA_PCWorkRequest fdapcwr = new FDA_PCWorkRequest();
        fdapcwr.setDrug((Drug) drugjComboBox1.getSelectedItem());
        fdapcwr.setMessage(messagejTextArea1.getText());
        fdapcwr.setSender(userAccount);
        fdapcwr.setRequestDate(reqDate);
        fdapcwr.setStatus("Request sent");

        for (Network n : ecosystem.getcountryNetworkList()) {
            for (StateNetwork sn : n.getStateNetworkList()) {
                for (CityNetwork cn : sn.getCityNetworkList()) {
                    for (Enterprise e : cn.getEnterpriseDirectory().getEnterpriseList()) {
                        if (e instanceof PharmaceuticalCompanyEnterprise) {
                            PharmaceuticalCompanyEnterprise pce = (PharmaceuticalCompanyEnterprise) e;

                            if (pce.getName().equals(fdapcwr.getDrug().getManufacturer())) {
                                for (Organization org : pce.getOrganizationDirectory().getOrganizationList()) {
                                    if (org instanceof PharmaceuticalCompanyMgrOrganization) {
                                        org.getWorkQueue().getWorkList().add(fdapcwr);
                                        userAccount.getWorkQueue().getWorkList().add(fdapcwr);
                                    }
                                }
                            }

                        }
                    }
                }
            }

        }
        populateTable();
    }//GEN-LAST:event_sendjButton1ActionPerformed

