    private void populateCombobox() {
        drugjComboBox1.removeAllItems();
        for (Drug drug : fdaEnterprise.getDrugCatalog().getDrugList()) {
            drugjComboBox1.addItem(drug);
        }

    }

    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) feedbacktablejTable1.getModel();
        model.setRowCount(0);
        for (WorkRequest wr : userAccount.getWorkQueue().getWorkList()) {
            if (wr instanceof FDA_PCWorkRequest) {
                FDA_PCWorkRequest request = (FDA_PCWorkRequest) wr;
                Object[] row = new Object[10];
                row[0] = request.getDrug();
                row[1] = request.getDrug().getDrugNo();
                row[2] = request.getMessage();
                row[3] = request.getRequestDate();
                row[4] = request.getReceiver();
                row[5] = request.getResolveDate();
                row[6] = request.getStatus();

                model.addRow(row);
            }
        }
    }

